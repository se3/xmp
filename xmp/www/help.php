 <h2>Small summary about the usage of the Xtreamer Mod Pack.</h2>
 <p>In the left menu, in order of appearance:</p>
 <hr />
 <p>MAVVY FS<br />
   This PHP script is the base of the entire Web page. (Mavvy's old fakeshell)</p>
 <hr />
 <p>SKINS<br />
   Click on the skin image you want to install.<br />
Be patient, may take some time to install.
 <hr />
<p>FONTS<br />
You can change the base GUI font. Just put your font into /xmp/guifont library, after open the font webpage in the XMP. <br />IMPORTANT: always check the uploaded fonts CRC, because it can crash the system on the Xtreamer.</p>
